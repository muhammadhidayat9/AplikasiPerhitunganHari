    private void buttonHitungSelisihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHitungSelisihActionPerformed
        // Dapatkan tanggal awal dari JCalendar
    java.util.Date tanggalAwal = calendarAwal.getDate();

    // Konversi tanggal awal ke LocalDate
    LocalDate localTanggalAwal = tanggalAwal.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
    
    // Dapatkan tahun dan bulan dari tanggal yang dipilih
    int tahun = localTanggalAwal.getYear();
    int bulan = localTanggalAwal.getMonthValue();

    // Hitung tanggal terakhir di bulan yang sama
    LocalDate localTanggalAkhir = localTanggalAwal.withDayOfMonth(localTanggalAwal.lengthOfMonth());

    // Hitung selisih hari antara tanggal awal dan tanggal terakhir bulan
    long selisihHari = ChronoUnit.DAYS.between(localTanggalAwal, localTanggalAkhir);

    // Tampilkan hasil selisih hari
    labelHasil.setText("Selisih " + selisihHari + " hari dari hari terakhir dibulan ini");

    }//GEN-LAST:event_buttonHitungSelisihActionPerformed

