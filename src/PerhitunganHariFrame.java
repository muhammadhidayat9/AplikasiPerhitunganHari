    private void updateCalendarDate() {
        int bulan = comboBoxBulan.getSelectedIndex() + 1; // Tambahkan 1 karena Januari = 1 di LocalDate
    
    // Ambil tahun dari JSpinner
    int tahun = (int) spinnerTahun.getValue();

    // Buat LocalDate dengan hari pertama di bulan dan tahun yang dipilih
    LocalDate date = LocalDate.of(tahun, bulan, 1);

    // Konversi LocalDate ke java.util.Date untuk diatur pada JCalendar
    java.util.Date tanggal = java.util.Date.from(date.atStartOfDay(ZoneId.systemDefault()).toInstant());
    
    // Set tanggal pada JCalendar
    calendarAwal.setDate(tanggal);
    }
