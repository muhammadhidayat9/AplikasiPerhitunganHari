    private void buttonHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHitungActionPerformed
        // TODO add your handling code here:
        // Dapatkan bulan dari JComboBox
    String namaBulan = comboBoxBulan.getSelectedItem().toString();
    int bulan = comboBoxBulan.getSelectedIndex() + 1; // Januari = 0

    // Dapatkan tahun dari JSpinner
    int tahun = (int) spinnerTahun.getValue();

    // Membuat LocalDate
    LocalDate date = LocalDate.of(tahun, bulan, 1);
    LocalDate firstDay = date.withDayOfMonth(1);
    LocalDate lastDay = date.withDayOfMonth(date.lengthOfMonth());


    // Menghitung jumlah hari
    int jumlahHari = date.lengthOfMonth();
    boolean tahunKabisat = date.isLeapYear();

    // Menampilkan hasil
    labelHasil.setText("Jumlah hari: " + jumlahHari + (tahunKabisat ? " (Tahun Kabisat)" : ""));
    labelHariPertama.setText("Hari pertama: " + firstDay.getDayOfWeek());
    labelHariTerakhir.setText("Hari terakhir: " + lastDay.getDayOfWeek());
    }//GEN-LAST:event_buttonHitungActionPerformed

